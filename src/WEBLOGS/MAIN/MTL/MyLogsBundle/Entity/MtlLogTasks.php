<?php

namespace WEBLOGS\MAIN\MTL\MyLogsBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * MtlLogTasks
 */
class MtlLogTasks
{
    /**
     * @var string
     */
    private $LOG_ID;

    /**
     * @var string
     */
    private $TASK;


    /**
     * Set LOG_ID
     *
     * @param string $lOGID
     * @return MtlLogTasks
     */
    public function setLOGID($lOGID)
    {
        $this->LOG_ID = $lOGID;

        return $this;
    }

    /**
     * Get LOG_ID
     *
     * @return string 
     */
    public function getLOGID()
    {
        return $this->LOG_ID;
    }

    /**
     * Set TASK
     *
     * @param string $tASK
     * @return MtlLogTasks
     */
    public function setTASK($tASK)
    {
        $this->TASK = $tASK;

        return $this;
    }

    /**
     * Get TASK
     *
     * @return string 
     */
    public function getTASK()
    {
        return $this->TASK;
    }
}
